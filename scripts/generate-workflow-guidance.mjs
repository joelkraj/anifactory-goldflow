#!/usr/bin/env node

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  PIPELINE_STAGE_REGISTRY,
  PIPELINE_STAGE_REGISTRY_VERSION,
  productionOrderSummary,
} from "./lib/pipeline-stage-registry.mjs";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const checkOnly = process.argv.includes("--check");
const targets = [
  path.join(repoRoot, "AGENTS.md"),
  path.join(repoRoot, "docs", "workflows", "video_production_workflow.md"),
];
const startMarker = "<!-- GOLDFLOW_STAGE_REGISTRY:START -->";
const endMarker = "<!-- GOLDFLOW_STAGE_REGISTRY:END -->";

function generatedBlock() {
  const rows = PIPELINE_STAGE_REGISTRY.map((stage) => (
    `| ${stage.order + 1} | \`${stage.id}\` | ${stage.approval} | \`${stage.validator}\` |`
  ));
  return [
    startMarker,
    `Registry version: \`${PIPELINE_STAGE_REGISTRY_VERSION}\`. This block is generated by \`npm run docs:workflow\`; edit \`scripts/lib/pipeline-stage-registry.mjs\`, not this table.`,
    "",
    "| # | Stage | Approval | Validator |",
    "| ---: | --- | --- | --- |",
    ...rows,
    "",
    `Command order: \`${productionOrderSummary()}\``,
    endMarker,
  ].join("\n");
}

function replaceBlock(text, block, filePath) {
  const start = text.indexOf(startMarker);
  const end = text.indexOf(endMarker);
  if (start < 0 || end < start) throw new Error(`Missing generated workflow markers in ${filePath}`);
  return `${text.slice(0, start)}${block}${text.slice(end + endMarker.length)}`;
}

async function main() {
  const block = generatedBlock();
  const stale = [];
  for (const filePath of targets) {
    const current = await fs.readFile(filePath, "utf8");
    const expected = replaceBlock(current, block, filePath);
    if (current === expected) continue;
    stale.push(path.relative(repoRoot, filePath));
    if (!checkOnly) await fs.writeFile(filePath, expected, "utf8");
  }
  if (checkOnly && stale.length) {
    throw new Error(`Generated workflow guidance is stale: ${stale.join(", ")}. Run npm run docs:workflow.`);
  }
  console.log(JSON.stringify({ status: "passed", check_only: checkOnly, updated: checkOnly ? [] : stale }, null, 2));
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
